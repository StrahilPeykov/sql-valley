CREATE TABLE customer ( 
    cID Integer,
    cName Text,
    street Text,
    city Text,
    PRIMARY KEY (cID));

CREATE TABLE store ( 
    sID Integer,
    sName Text,
    street Text,
    city Text,
    PRIMARY KEY (sID));

CREATE TABLE product ( 
    pID Integer,
    pName Text,
    suffix Text,
    PRIMARY KEY (pID));

CREATE TABLE shoppinglist ( 
    cID Integer,
    pID Integer,
    quantity Integer,
    date Date,
    PRIMARY KEY (cID, pID, date),
    FOREIGN KEY (cID) REFERENCES customer(cID),
    FOREIGN KEY (pID) REFERENCES product(pID));

CREATE TABLE purchase (
    tID Integer,
    cID Integer,
    sID Integer,
    pID Integer,
    date Date,
    quantity Integer,
    price Decimal(10, 2),
    PRIMARY KEY (tID, pID),
    FOREIGN KEY (cID) REFERENCES customer(cID),
    FOREIGN KEY (sID) REFERENCES store(sID),
    FOREIGN KEY (pID) REFERENCES product(pID));

CREATE TABLE inventory ( 
    sID Integer,
    pID Integer,
    date Date,
    quantity Integer,
    unit_price Decimal(10, 2),
    PRIMARY KEY (sID, pID, date),
    FOREIGN KEY (sID) REFERENCES store(sID),
    FOREIGN KEY (pID) REFERENCES product(pID));
	
